# Python is a case sensitive language
print('Hello World')

print('hello',3,7.0,True)
#sep='/' (it is used to replace the space b/t 'hello',3,7.0,True by '/')
print('hello',3,7.0,True,sep='/')

print('hello')
print('world')
#if you want to replace the next line of hello world then you use end='-' after 'hello'
print('hello',end='-')
print('world')

