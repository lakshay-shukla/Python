# Keywords (They are reserved by Compiler or Interpreter so they are not used in code as a variable)
# Identifiers (Make a function , variables, class name or creating a name)
# Rules of identifiers -:
#1 You can't start with a digit
name1 = 'Lakshay'
print(name1)

#2 You can use special chars --> _
first_name = 'Lakshay'
print(first_name)
# or
_ = 'Lakshay'
print(_)

#3 identifiers can't be keyword (True, False, if, else etc)